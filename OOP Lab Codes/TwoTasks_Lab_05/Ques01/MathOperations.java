package Ques01;

public class MathOperations {
    // multiply two integers
    public int multiply(int a, int b) {
        return a * b;
    }

    // multiply two doubles
    public double multiply(double a, double b) {
        return a * b;
    }

    // multiply three integers
    public int multiply(int a, int b, int c) {
        return a * b * c;
    }
}
